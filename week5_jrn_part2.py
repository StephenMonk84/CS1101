def string_slice1(str1):
    print(str1[33:54])

def string_slice2(str1):
    print(str1[55:])

def string_slice3(str1):
    print(str1[:32])

    
string_in = "Sometimes you want the beginning, sometimes the middle and sometimes the end"
string_slice1(string_in)
string_slice2(string_in)
string_slice3(string_in)